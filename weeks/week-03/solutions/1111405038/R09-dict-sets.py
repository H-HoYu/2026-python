# R9. 兩字典相同點：keys/items 集合運算（1.9）

# 兩個字典資料
a = {'x': 1, 'y': 2, 'z': 3}
b = {'w': 10, 'x': 11, 'y': 2}

# 找出兩個字典都有的鍵（交集）
a.keys() & b.keys()

# 找出只在 a 中出現、但不在 b 中的鍵（差集）
a.keys() - b.keys()

# 找出兩個字典中完全相同的 (鍵, 值) 配對
a.items() & b.items()

# 用字典生成式建立新字典，排除鍵 'z' 與 'w'
c = {k: a[k] for k in a.keys() - {'z', 'w'}}
