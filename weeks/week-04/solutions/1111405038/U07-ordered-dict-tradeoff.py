# U7. OrderedDict 的取捨：保序但更吃記憶體（1.7）

from collections import OrderedDict

# OrderedDict 是一種會明確維護插入順序的字典型別。
# 也就是說，之後在迭代鍵值時，會依照加入的先後順序輸出。
d = OrderedDict()
d['foo'] = 1
d['bar'] = 2

# 在這個例子中，迭代 d 時會先看到 foo，再看到 bar。
#
# 為了做到這件事，OrderedDict 需要額外的內部結構來記錄元素順序，
# 例如每個項目之間的前後關係。
# 這代表它相較於一般 dict，通常會用掉更多記憶體，
# 也可能在某些操作上帶來一些額外成本。
#
# 不過要注意：在現代 Python（3.7+）中，一般 dict 也會保留插入順序。
# 因此如果你只是想「保留加入順序」，很多情況下直接用 dict 就夠了。
# OrderedDict 真正比較有價值的場景，通常是你需要它提供的順序相關操作語意，
# 或是想明確表達「這個映射的順序是設計重點」。
