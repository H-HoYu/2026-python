# U10. zip 為何只能用一次（1.8）

# 這裡準備一個簡單字典，等等會把 values 與 keys 配對在一起。
prices = {'A': 2.0, 'B': 1.0}

# 在 Python 3 中，zip() 回傳的不是 list，而是 iterator（迭代器）。
# 也就是說，它的元素不是一開始就全部存好，
# 而是在你需要時才一個一個產生出來。
z = zip(prices.values(), prices.keys())

# min(z) 會從 z 中逐一取出資料來比較，直到整個 iterator 被走完。
# 一旦走完，z 就被「消耗」掉了，裡面不會自動重置。
min(z)  # OK（消耗掉迭代器）

# 因此如果之後再拿同一個 z 去做 max(z)，
# z 已經沒有任何元素可供迭代。
# 這類一次性 iterator 若想重用，通常要重新建立一次，
# 或先用 list(zip(...)) 把內容保存下來。
# max(z)  # 會失敗：因為 z 已經被消耗完
