# U3. deque(maxlen=N) 為何能保留最後 N 筆（1.3）

from collections import deque

# deque 是雙端佇列，適合從左或右高效率地加入/移除元素。
# 這裡設定 maxlen=3，表示這個 deque 最多只能保留 3 筆資料。
q = deque(maxlen=3)

# 依序把 1, 2, 3, 4, 5 加到右邊。
# 前三次 append 後，內容會依序變成：
# [1] -> [1, 2] -> [1, 2, 3]
#
# 當再加入 4 時，因為容量上限是 3，最左邊最舊的 1 會自動被丟掉，
# 變成 [2, 3, 4]。
# 再加入 5 時，同理會淘汰最舊的 2，最後得到 [3, 4, 5]。
for i in [1, 2, 3, 4, 5]:
    q.append(i)
# 因此 deque(maxlen=N) 很適合拿來保留「最近 N 筆資料」，
# 例如最近幾次輸入、最近幾行日誌、滑動視窗等情境。
# 結果只剩 [3, 4, 5]
