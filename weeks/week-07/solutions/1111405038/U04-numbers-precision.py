# U04. 數字精度的陷阱與選擇（3.1–3.7）
# ============================================================
# 本檔案涵蓋三個與數字精度相關的常見陷阱：
#   1. 銀行家捨入（Banker's Rounding）：Python round() 的行為與直覺不同
#   2. NaN 比較陷阱：float('nan') 不等於任何值（包括自己)
#   3. float vs Decimal 的選擇依據：速度 vs 精確度的取捨
# ============================================================

import math
import timeit
from decimal import Decimal, ROUND_HALF_UP  # Decimal 精確小數；ROUND_HALF_UP 傳統四捨五入模式

# ============================================================
# 陷阱 1：銀行家捨入（3.1）
# ============================================================
# Python 內建的 round() 採用「四捨六入五取偶」（Round Half to Even），
# 也稱為「銀行家捨入」，是 IEEE 754 浮點數標準的預設行為。
#
# 規則：當小數部分恰好為 0.5 時，捨入到「最近的偶數」。
#   round(0.5) → 0（偶數），不是 1
#   round(2.5) → 2（偶數），不是 3
#   round(3.5) → 4（偶數），不是 3
#
# 設計目的：大量數值運算時，傳統四捨五入會讓結果系統性偏大；
# 銀行家捨入讓一半向上、一半向下，長期統計誤差趨近於零，
# 因此適合金融及科學計算，但與日常習慣不符。
# ============================================================
# Python round() 用「四捨六入五取偶」，不是日常四捨五入
print(round(0.5))  # 0（不是 1！偶數優先）
print(round(2.5))  # 2（不是 3！偶數優先）
print(round(3.5))  # 4（4 是偶數，符合規則）


# 若需傳統四捨五入（小學教的「逢五進一」），改用 Decimal + ROUND_HALF_UP
def trad_round(x: float, n: int = 0) -> Decimal:
    # 步驟 1：用 str() 轉換 float，避免浮點誤差（直接 Decimal(0.1) 會有精度問題）
    d = Decimal(str(x))
    # 步驟 2：決定量化格式
    #   n=0 → 捨入到個位，格式為 "1"
    #   n=2 → 捨入到小數第 2 位，格式為 "0.01"
    fmt = Decimal("1") if n == 0 else Decimal("0." + "0" * n)
    # 步驟 3：量化並指定捨入模式
    return d.quantize(fmt, rounding=ROUND_HALF_UP)


print(trad_round(0.5))  # 1（傳統四捨五入）
print(trad_round(2.5))  # 3（傳統四捨五入）

# ============================================================
# 陷阱 2：NaN 無法用 == 比較（3.7）
# ============================================================
# NaN（Not a Number）是浮點數規格（IEEE 754）中的特殊值，
# 代表「無意義的數值結果」，例如 0.0/0.0、sqrt(-1) 等運算的產物。
#
# 關鍵特性：NaN != NaN（NaN 不等於任何值，包括它自己！）
# 這是 IEEE 754 的規範，目的是讓程式能偵測「計算鏈中是否出現了無效值」。
#
# 因此唯一正確的 NaN 檢測方式是 math.isnan() 或 numpy.isnan()，
# 絕對不能用 == 或 is 比較。
# ============================================================
c = float("nan")  # 建立 NaN 值
print(c == c)           # False！NaN 自己不等於自己（IEEE 754 規範）
print(c == float("nan"))  # False（同理，兩個不同的 NaN 也不相等）
print(math.isnan(c))    # True — 這才是檢測 NaN 的正確方式

# 實際應用：從資料清單中過濾 NaN
data = [1.0, float("nan"), 3.0, float("nan"), 5.0]
# 注意：不能用 x != float("nan") 過濾，因為 NaN != NaN 永遠為 True
clean = [x for x in data if not math.isnan(x)]
print(clean)  # [1.0, 3.0, 5.0]

# ============================================================
# 陷阱 3：float vs Decimal 的選擇（3.2）
# ============================================================
# float（IEEE 754 雙精度）：
#   - 底層以二進位分數（binary fraction）儲存，無法精確表示大多數十進位小數
#   - 0.1 實際儲存為 0.1000000000000000055511151231257827021181583404541015625
#   - 兩個「看似相等」的 float 計算後可能不相等
#   - 優點：硬體原生支援，速度極快；適合科學計算、物理模擬
#
# Decimal（十進位精確表示）：
#   - 以十進位字串為基礎，可精確表示任意十進位小數
#   - 支援可設定精度與多種捨入模式，符合金融計算標準
#   - 缺點：純 Python 實作（無硬體加速），速度約慢 10–50 倍
#   - 適合：金融、稅務、會計等對精度有嚴格要求的場景
#
# 記憶口訣：科學用 float（快），金融用 Decimal（準）
# ============================================================
# float：快但有誤差（科學/工程適用）
print(0.1 + 0.2)          # 0.30000000000000004（二進位表示的累積誤差）
print(0.1 + 0.2 == 0.3)   # False（浮點不精確，直接 == 比較危險）

# Decimal：精確但慢（金融/會計適用）
# 重要：必須用字串 "0.1" 初始化，而非 Decimal(0.1)（後者會繼承 float 的誤差）
print(Decimal("0.1") + Decimal("0.2"))            # 0.3（精確）
print(Decimal("0.1") + Decimal("0.2") == Decimal("0.3"))  # True

t1 = timeit.timeit(lambda: 0.1 * 999, number=100_000)
t2 = timeit.timeit(lambda: Decimal("0.1") * 999, number=100_000)
print(f"float: {t1:.3f}s  Decimal: {t2:.3f}s（Decimal 約慢 {t2 / t1:.0f} 倍）")
