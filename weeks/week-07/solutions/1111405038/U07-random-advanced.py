# U07. 隨機種子與安全亂數（3.11）
# ============================================================
# 本檔案涵蓋兩個主題：
#   1. random 模組是「偽隨機」（PRNG），相同種子必產生相同序列
#   2. 密碼學安全場景必須用 secrets 模組，絕不能用 random
# ============================================================

import random   # 底層使用 Mersenne Twister（MT19937）算法
import secrets  # Python 3.6+ 內建，底層使用作業系統的密碼學亂數來源

# ============================================================
# 1. 相同種子 → 相同序列（可重現性）
# ============================================================
# Mersenne Twister 是「確定性算法」：給定相同的初始狀態（種子），
# 往後產生的全部數字序列完全一致。
#
# 應用場景：
#   ✓ 可重現的科學實驗（固定種子從而可重現結果）
#   ✓ 遊戲測試 / 節點高分排行榜測試
#   ✗ 任何安全相關用途（完全不適合）
# ============================================================
random.seed(42)  # 設定全域種子為 42（可用任意整數、字串或 bytes）
seq1 = [random.randint(1, 100) for _ in range(5)]
random.seed(42)  # 重置為相同種子——狀態完全回到起點
seq2 = [random.randint(1, 100) for _ in range(5)]
print(seq1 == seq2)  # True：兩次產生完全相同的序列

# ============================================================
# 2. 不同 Random 實例各自獨立
# ============================================================
# random 模組的全域函式（random.randint 等）共用一個內建的全域狀態機。
# 若需要多個彼此不干擾的隨機流（如並行拼型節高分計算），
# 應建立獨立的 random.Random 實例，每個實例擁有自己獨立的內部狀態。
rng1 = random.Random(1)  # 種子 1，獨立狀態機
rng2 = random.Random(2)  # 種子 2，完全不影響 rng1
print(rng1.random(), rng2.random())  # 各自產生自己的亂數流
# rng1 跟 rng2 互不干擾，也不影響全域 random 狀態

# ============================================================
# 3. 密碼學安全亂數（secrets 模組）
# ============================================================
# secrets 的來源：
#   Linux/macOS → /dev/urandom（擷取硬體雜訊、中斷時間等）
#   Windows     → BCryptGenRandom / CryptGenRandom
#
# 與 random 模組的關鍵差異：
#   • 不可預測：即使知道過去的輸出也無法推測未來值
#   • 不能設種子：沒有 secrets.seed()，每次結果都不同
#   • 符合密碼學安全標準，適合密碼、token、session key
#
# 安全經驗法則：
#   隨機密碼 / OTP / session token / CSRF token / API token → 一律用 secrets
#   模擬 / 測試 / 洗牌 / 隨機抽樣 → 可用 random
# ============================================================
print(secrets.randbelow(100))   # [0, 100) 密碼學安全整數
print(secrets.token_hex(16))    # 32 個十六進位字元的隨機字串（適合 token、API key）
print(secrets.token_bytes(16))  # 16 個隨機字組（適合密鑰、session 種子）
# token_hex(16) 產生 128-bit 亂數，現代密碼學建議至少 128 bit
